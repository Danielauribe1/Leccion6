let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma: " + z );

z = a - b;
console.log("Resultado de la resta: " + z);

z = a * b;
console.log( "Resultado de la multiplicación:" + z);

z = a / b;
console.log( "Resultado de la división:" + z);

z = a % b;//residuo de la division
console.log( "Resultado de operacion modulo (residuo):" + z);

z = a ** b; //Exponente 
console.log( "Resultado del operador exponente:" + z);